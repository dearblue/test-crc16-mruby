static const uint16_t table[16][256];

uint16_t
crc16_by1_octet(const uint8_t *ptr, size_t num, uint16_t crc)
{
    for (; num > 0; num --, ptr ++) {
        crc = table[0][(crc >>  8) & 0xff] ^
              (crc << 8) ^ *ptr;
    }

    return crc;
}

uint16_t
crc16_by2_octet(const uint8_t *ptr, size_t num, uint16_t crc)
{
    for (; num >= 2; num -= 2, ptr += 2) {
        crc = table[1][(crc >>  8) & 0xff] ^
              table[0][(crc >>  0) & 0xff] ^
              (ptr[0] << 8) ^ ptr[1];
    }

    for (; num > 0; num --, ptr ++) {
        crc = table[0][(crc >>  8) & 0xff] ^
              (crc << 8) ^ *ptr;
    }

    return crc;
}

uint16_t
crc16_by4_octet(const uint8_t *ptr, size_t num, uint16_t crc)
{
    for (; num >= 4; num -= 4, ptr += 4) {
        uint8_t a = ptr[0];
        uint8_t b = ptr[1];
        uint16_t cd = (ptr[2] << 8) | ptr[3]; /* load uint16_t as big-endian */

        crc = table[3][(crc >>  8) & 0xff] ^
              table[2][(crc >>  0) & 0xff] ^
              table[1][(a   >>  0) & 0xff] ^
              table[0][(b   >>  0) & 0xff] ^
              cd;
    }

    for (; num > 0; num --, ptr ++) {
        crc = table[0][(crc >>  8) & 0xff] ^
              (crc << 8) ^ *ptr;
    }

    return crc;
}

uint16_t
crc16_by8_octet(const uint8_t *ptr, size_t num, uint16_t crc)
{
    for (; num >= 8; num -= 8, ptr += 8) {
        uint8_t a = ptr[0];
        uint8_t b = ptr[1];
        uint8_t c = ptr[2];
        uint8_t d = ptr[3];
        uint8_t e = ptr[4];
        uint8_t f = ptr[5];
        uint16_t gh = (ptr[6] << 8) | ptr[7]; /* load uint16_t as big-endian */

        crc = table[7][(crc >>  8) & 0xff] ^
              table[6][(crc >>  0) & 0xff] ^
              table[5][(a   >>  0) & 0xff] ^
              table[4][(b   >>  0) & 0xff] ^
              table[3][(c   >>  0) & 0xff] ^
              table[2][(d   >>  0) & 0xff] ^
              table[1][(e   >>  0) & 0xff] ^
              table[0][(f   >>  0) & 0xff] ^
              gh;
    }

    for (; num > 0; num --, ptr ++) {
        crc = table[0][(crc >>  8) & 0xff] ^
              (crc << 8) ^ *ptr;
    }

    return crc;
}

uint16_t
crc16_by16_octet(const uint8_t *ptr, size_t num, uint16_t crc)
{
    for (; num >= 16; num -= 16, ptr += 16) {
        uint8_t a = ptr[ 0];
        uint8_t b = ptr[ 1];
        uint8_t c = ptr[ 2];
        uint8_t d = ptr[ 3];
        uint8_t e = ptr[ 4];
        uint8_t f = ptr[ 5];
        uint8_t g = ptr[ 6];
        uint8_t h = ptr[ 7];
        uint8_t i = ptr[ 8];
        uint8_t j = ptr[ 9];
        uint8_t k = ptr[10];
        uint8_t l = ptr[11];
        uint8_t m = ptr[12];
        uint8_t n = ptr[13];
        uint16_t op = (ptr[14] << 8) | ptr[15]; /* load uint16_t as big-endian */

        crc = table[15][(crc >>  8) & 0xff] ^
              table[14][(crc >>  0) & 0xff] ^
              table[13][(a   >>  0) & 0xff] ^
              table[12][(b   >>  0) & 0xff] ^
              table[11][(c   >>  0) & 0xff] ^
              table[10][(d   >>  0) & 0xff] ^
              table[ 9][(e   >>  0) & 0xff] ^
              table[ 8][(f   >>  0) & 0xff] ^
              table[ 7][(g   >>  0) & 0xff] ^
              table[ 6][(h   >>  0) & 0xff] ^
              table[ 5][(i   >>  0) & 0xff] ^
              table[ 4][(j   >>  0) & 0xff] ^
              table[ 3][(k   >>  0) & 0xff] ^
              table[ 2][(l   >>  0) & 0xff] ^
              table[ 1][(m   >>  0) & 0xff] ^
              table[ 0][(n   >>  0) & 0xff] ^
              op;
    }

    for (; num > 0; num --, ptr ++) {
        crc = table[0][(crc >>  8) & 0xff] ^
              (crc << 8) ^ *ptr;
    }

    return crc;
}

